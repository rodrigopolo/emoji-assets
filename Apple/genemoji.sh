function optimize(){
	cd $1
	FILES=*.png
	for f in $FILES
	do
		OPTI=opti.$f
		pngquant --speed=1 --quality=0-100 $f -o $OPTI
		optipng -quiet -o7 $OPTI
		advpng -q -z -4 $OPTI
		advdef -q -z -4 $OPTI
		mv $OPTI $f
	done
	cd "$SCRIPTPATH"
}


